    # Create the PR
    PR_OUTPUT=$(gh pr create \
        --title "fix: Resolve issue #$ISSUE_NUMBER" \
        --body "$PR_BODY" \
        --base main \
        --head "$BRANCH_NAME" \
        --assignee "@me")
    
    # Extract PR number and URL
    PR_URL="$PR_OUTPUT"
    PR_NUMBER=$(echo "$PR_URL" | grep -oE '[0-9]+$')
fi

